# ---------------------------------------------------------------------------
# 1. SKELETON_PROMPT — 骨架提取
# ---------------------------------------------------------------------------
# 输入：6 个 500 字的等距采样片段
# 输出：5-8 行目录骨架
# max_tokens: 256

SKELETON_PROMPT = (
    "你是一位内容结构分析师。下面是一段长内容中等距采样的 6 个片段（各约 500 字）。\n\n"
    "【任务】根据这些片段推断全文的主题走向，输出一份 5-8 行的目录骨架。\n"
    "每行是一个章节标题，按内容出现的时间顺序排列。\n\n"
    "【格式】\n"
    "- 每行一个标题，不加编号、不加符号\n"
    "- 标题必须是判断句或名词短语，不是疑问句\n"
    "- 5-8 行，不多不少\n\n"
    "【正确示例】\n"
    "AI 芯片算力瓶颈与解决路径\n"
    "开源模型在企业落地的三个障碍\n"
    "Transformer 架构的本质优势\n\n"
    "【错误示例】\n"
    "第一部分：引言（太笼统，无信息量）\n"
    "AI 是什么？（疑问句，不是判断）\n"
    "关于技术的讨论（话题词，不是结论）\n\n"
    "【禁止】\n"
    "- 禁止输出超过 8 行\n"
    "- 禁止使用「引言」「总结」「结语」等通用标签\n"
    "- 禁止添加任何解释性文字，只输出标题列表\n\n"
    "【语言要求】输出全程中文。"
)

# ---------------------------------------------------------------------------
# 2. CHUNK_SUMMARY_PROMPT_TEMPLATE — 单块摘要模板
# ---------------------------------------------------------------------------
# 变量：skeleton, idx, total, overlap_hint
# 两阶段思维：先提取关键事实，再结构化重组

CHUNK_SUMMARY_PROMPT_TEMPLATE = (
    "你是一位信息提取专家。你正在处理一段长内容的第 {idx}/{total} 块。\n\n"
    "【全局骨架】（供你定位当前块在全文中的位置）\n"
    "{skeleton}\n\n"
    "{overlap_hint}"
    "【两阶段处理】\n"
    "第一阶段 — 提取：从本块中找出所有关键事实（数据、因果关系、人名/机构名、结论）。\n"
    "第二阶段 — 重组：将提取的事实按逻辑关系组织成连贯段落。\n\n"
    "【输出要求】\n"
    "- 不超过 350 字\n"
    "- 只保留最高价值信息：优先数据 > 因果链 > 具体案例 > 一般性论述\n"
    "- 如果本块信息密度低（纯过渡/闲聊），输出可以短于 200 字\n"
    "- 每句话必须包含具体信息（数据、因果、名称中至少一项）\n"
    "- 关键数据用 **加粗**\n"
    "- 如有对比关系，用「A vs B：结论」格式\n\n"
    "【正确示例】\n"
    "OpenAI 在 2024 年将 GPT-4 推理成本降低了 **12 倍**，主要通过 "
    "KV-cache 优化和批量推理实现。这使得单次 API 调用成本从 $0.06 降至 $0.005，"
    "直接推动了 AI Agent 从实验进入生产环境。\n\n"
    "【错误示例】\n"
    "本段讲述了 AI 成本下降的趋势。（元描述，无具体信息）\n"
    "视频介绍了多种优化技术。（弱动词 + 无细节）\n\n"
    "【禁止】\n"
    "- 禁止元描述（「本段讲述了…」「这部分介绍了…」「作者提到…」）\n"
    "- 禁止省略数据只留结论\n"
    "- 禁止使用「等」来替代具体列举\n"
    "- 禁止超过 350 字\n\n"
    "【语言要求】输出全程中文。"
)

